import tensorflow as tf
# 原本的是2*3*4
# 转变之后的变成了3*2*4

a2 = [[[1, 2, 3, 5],
       [4, 5, 6, 5],
       [7, 8, 9, 5]],
       
      [[10, 11, 12, 6],
       [13, 14, 15, 6],
       [16, 17, 18, 6]]]


b2 = tf.transpose(a2, perm=[1, 0, 2])

with tf.Session() as sess:
    print(sess.run(b2))

